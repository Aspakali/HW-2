function addition(num1, num2, num3, num4) {
    return num1+num2+num3+num4;
}
function subtraction(num1, num2, num3, num4){
    return num1-num2-num3-num4;
}
function multiplication(num1, num2, num3, num4){
    return num1*num2*num3*num4;
}
function division(num1, num2, num3, num4) {
    return num1/num2/num3/num4;
}

console.log(addition(7,5,6,3));
console.log(subtraction(7,5,6,3));
console.log(multiplication(7,5,6,3));
console.log(division(7,5,6,3));